USE Colegiu
GO

CREATE TABLE Specialitate(
             IdSpec INT PRIMARY KEY,
			 NumeSpec CHAR(50)) 
			 GO
CREATE TABLE Raion(
             IdRaion INT PRIMARY KEY,
			 NumeRaion CHAR(20))
			 GO
CREATE TABLE Profesori(
             IdProf INT PRIMARY KEY,
			 NumeProf CHAR(20),
			 PrenumeProf CHAR(20))
			 GO
CREATE TABLE Grupa(
             IdGrupa INT PRIMARY KEY,
			 NumeGrupa CHAR(10),
			 AnAdmis INT,
			 IdProf INT REFERENCES Profesori(IdProf),
			 IdSpec INT REFERENCES Specialitate(IdSpec))

CREATE TABLE Localitate(
             IdLoc INT PRIMARY KEY,
			 NumeLoc CHAR(20),
			 IdRaion INT REFERENCES Raion(IdRaion))
			 GO
CREATE TABLE Catedre(
             IdCatedra INT PRIMARY KEY,
			 NumeCatedra CHAR(40),
			 IdProf INT REFERENCES Profesori(IdProf))
			 GO
CREATE TABLE Discipline(
             IdObiect INT PRIMARY KEY,
			 NumeObiect CHAR(40),
			 IdCatedra INT REFERENCES Catedre(IdCatedra))
			 GO
CREATE TABLE Student(
             IdStud INT PRIMARY KEY,
			 NumeStud CHAR(10),
			 PrenStud CHAR(20),
			 PatrStud CHAR(20),
			 SexStud CHAR(1) CHECK (SexStud='M' OR SexStud='F'),
			 DataNasterii DATE,
			 GSM CHAR(20),
			 IdGrupa INT REFERENCES Grupa(IdGrupa),
			 IdLoc INT REFERENCES Localitate(IdLoc))
			 GO
CREATE TABLE DisciplineGrupa(
             IdDisGrupa INT PRIMARY KEY,
			 IdGrupa INT REFERENCES Grupa(IdGrupa),
			 IdObiect INT REFERENCES Discipline(IdObiect),
			 IdProf INT REFERENCES Profesori(IdProf))
			 GO
CREATE TABLE Note(
             IdNota INT PRIMARY KEY,
			 IdStud INT REFERENCES Student(IdStud),
			 IdObiect INT REFERENCES Discipline(IdObiect),
			 DataNota DATE,
			 Nota INT)
GO
